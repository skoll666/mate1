#Ejercicio 3: Conversión de Tipos de Datos
g = 7 #En esta variable se coloca una variable con un número entero.
h = 2.5 #En esta variable se coloca una variable con un número flotante.
i = float (g) #En la variable float() se utiliza para convertir el número entero a uno flotante.
j = int (h) #En la variable int() se utiliza para convertir el número flotante a uno entero.
print (g,i)
print (h,j)
#El propósito de este ejercicio u programa es transformas un número entero a flotante y uno flotante a entero.