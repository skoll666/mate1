print ("el resultado de la multiplicación entre 5*5 es:", c)
print ("el resultado de la división entre 16.8/4.2 es:", d)
#El propósito de este ejercicio u programa es realizar suma, resta, multiplicación y división, ya sea de numeros enteros o flotantes.

#Ejercicio 2: Manipulación de Cadenas de Texto
