#El propósito general de este programa es realizar Operaciones Básicas y Tipos de Datos, Manipulación de Cadenas de Texto y Conversión de Tipos de Datos.
#Ejercicio 1: Operaciones Básicas y Tipos de Datos
a = 30+20 #En esta variable se puede realizar la suma de números enteros.
b = 2.4-1.2 #En esta variable se puede realizar la resta de números flotantes.
c = 5*5 #En esta variable se puede realizar la multiplicación de números enteros.
d = 16.8/4.2 #En esta variable se puede realizar la división de números flotantes.

print ("el resultado de la suma entre 30+20 es:", a)
